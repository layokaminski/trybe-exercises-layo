function sum(a, b) {
    return a + b
}

function subtraction(a, b) {
    return a - b
}

function multiplication(a, b) {
    return a * b
}

function division(a, b) {
    return a / b
}

function module(a, b) {
    return a % b
}

console.log(sum(3, 2));
console.log(subtraction(3, 2));
console.log(multiplication(3, 2));
console.log(division(3, 2));
console.log(module(3, 2));